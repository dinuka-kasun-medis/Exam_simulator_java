    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        int rowNo=selectExamTable.getSelectedRow();
        if (selectExamTable.getSelectedRow()>=0) {
            String exam_id=selectExamTable.getValueAt(rowNo, 0).toString();
            String examName=selectExamTable.getValueAt(rowNo, 1).toString();
            String subject=selectExamTable.getValueAt(rowNo, 2).toString();
            String unite=selectExamTable.getValueAt(rowNo, 3).toString();
            int numberOfQuestions=Integer.parseInt(selectExamTable.getValueAt(rowNo, 4).toString());
            examPaperPriviewPanel=new ExamPaperPriviewPanel(exam_id,examName,subject,unite,numberOfQuestions,admin_Main_Window);
            Admin_Main_Window.mainWindowPanel.removeAll();
            Admin_Main_Window.mainWindowPanel.add(examPaperPriviewPanel);
            Admin_Main_Window.mainWindowPanel.repaint();
            Admin_Main_Window.mainWindowPanel.revalidate();
            examPaperPriviewPanel.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Please select an exam!");
        }
        
    }//GEN-LAST:event_viewBtnActionPerformed


