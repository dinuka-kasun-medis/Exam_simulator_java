    /*Method for add load all exams to table Â© Dinuka Kasun Medis*/
    private void getAllExams(){
        try {
            ExamController examController=ServerConnector.getServerConnector().getExamController();
            ArrayList<Exam> examList=examController.getAllExam();
            DefaultTableModel dtm=(DefaultTableModel) selectExamTable.getModel();
            dtm.setRowCount(0);
            if (examList!=null) {
                for (Exam exam : examList) {
                    Object []raw={exam.getExam_id(),exam.getExam_name(),exam.getSubject(),exam.getUnite(),exam.getNo_of_questions()};
                    dtm.addRow(raw);
                }
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(SelectExamPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(SelectExamPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(SelectExamPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(SelectExamPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
