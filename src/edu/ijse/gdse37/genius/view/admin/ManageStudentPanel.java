    private void loadStudentList() {
        try {
            student_idCOmbo.removeAllItems();
            StudentController studentController = ServerConnector.getServerConnector().getStudentController();
            ArrayList<Student> studentList = studentController.getAllStudent();
            if (studentList != null) {
                for (Student student : studentList) {
                    student_idCOmbo.addItem(student.getStudent_id());
                }
            }
            new ComboSearch().setSearchableCombo(student_idCOmbo, true, "No Student found");
        } catch (NotBoundException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

