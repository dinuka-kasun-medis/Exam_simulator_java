    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            StudentController studentController = ServerConnector.getServerConnector().getStudentController();
            String student_id = student_idTxt.getText();
            String name = nameTxt.getText();
            String email = emailTxt.getText();
            int telephone = Integer.parseInt(telephoneTxt.getText());
            String password = Encription.encriptTxt(passwordTxt.getText());//encript(passwordTxt.getText());
            String nic = nicTxt.getText();
            Student student = new Student(student_id, name, nic, email, telephone, password);
            boolean isAdded = studentController.isAddStudent(student);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Student added sucsess...");
                student_idTxt.setVisible(false);
                newStudentBtn.setVisible(true);
                student_idCOmbo.setVisible(true);
                loadStudentList();
            } else {
                JOptionPane.showMessageDialog(this, "Somthing wrong...student add");
                newStudentBtn.setVisible(true);
                student_idCOmbo.setVisible(true);
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ManageStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

